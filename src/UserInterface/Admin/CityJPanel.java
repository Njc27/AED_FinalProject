    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(isEdit){
            City selCity = system.getCityDirectory().getCityByName(beforeEdit);
            String updatedName = jTextField1.getText();
            if(updatedName.trim().length()==0){
                JOptionPane.showMessageDialog(this, "City Field Empty");
            }
            else{
            selCity.setCityName(updatedName);
            jButton1.setText("Add City");
            isEdit = false;
            jTextField1.setText("");
            populateTable();
            JOptionPane.showMessageDialog(this, "City Updated"); 
            }
        }
        else{
            String city = jTextField1.getText();
            if(city.trim().length()==0){
                JOptionPane.showMessageDialog(this, "City Field Empty");
            }

            else if(system.getCityDirectory().getCityByName(city)!=null){
                JOptionPane.showMessageDialog(this, "City Already Exists");
            }
            else{
                City c = new City();
                c.setCityName(city);
                system.getCityDirectory().addCity(c);
                populateTable();
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Please Select a Row");
        }
        else{
            String city = (String) model.getValueAt(selectedRow,0);
            City selCity = system.getCityDirectory().getCityByName(city);
            if(selCity!=null){
                system.getCityDirectory().removeCity(selCity);
                populateTable();
            }
            else{
                JOptionPane.showMessageDialog(this, "Community might already be deleted");
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Please Select a Row");
        }
        else{
            String selectedCity =  (String) model.getValueAt(selectedRow,0);
            jTextField1.setText(selectedCity);
            jButton1.setText("Save");
            beforeEdit = selectedCity;
            isEdit = true;

        }
    }//GEN-LAST:event_jButton3ActionPerformed

   public void populateTable(){
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
         for (int i =0;i<system.getCityDirectory().getCityList().size();i++){
                        Object rowData[] = new Object[1];
                        rowData[0] = system.getCityDirectory().getCityList().get(i).getCityName();
                        model.addRow(rowData);
        }
    }

