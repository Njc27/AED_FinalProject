    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = ModreqTable.getSelectedRow();
        int rowIndex = selectedRowIndex;

        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please Select a row");
        }

        DefaultTableModel model = (DefaultTableModel) ModreqTable.getModel();
        EmergencySoS selectedPlan = (EmergencySoS) model.getValueAt(selectedRowIndex, 0);
        jTextField5.setText(String.valueOf(selectedPlan.getScence()));
        jTextField6.setText(String.valueOf(selectedPlan.getZipCode()));
        jTextField7.setText(String.valueOf(selectedPlan.getNoVictims()));
        jTextField8.setText(String.valueOf(selectedPlan.getLocation()));
    }//GEN-LAST:event_jButton8ActionPerformed

