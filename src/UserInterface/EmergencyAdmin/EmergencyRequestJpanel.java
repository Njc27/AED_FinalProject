    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) EmgReqTable.getModel();
        model.setRowCount(0);
        for(EmergencySoS es : system.getEmgSoSList().getEmgSoSList()){
            Object[] row = new Object[10];
            row[0] = es;
            row[1] = es.getZipCode();
            row[2] = es.getNoVictims();
            row[3] = es.getLocation();
            model.addRow(row); 
        }
        }
    
