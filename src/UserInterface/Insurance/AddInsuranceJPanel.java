    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String compName = CompNameTxtField.getText();
        String Plan = PlanNameTxtField.getText();
        String Validity = PlanValTxtField.getText();
        String amt = PlanAmtTxtField.getText();

        InsurancePlan InsPlan = new InsurancePlan();

        InsPlan.setCompanyName(compName);
        InsPlan.setPlanName(Plan);
        //InsPlan.setPremiumAmt(amt);

        String validtel = "\\d{4}";
        Pattern pattern = Pattern.compile(validtel);
        Matcher matcher = pattern.matcher(amt);
        if (!matcher.matches()){
            JOptionPane.showMessageDialog(this,"Enter correct amount");
            return;
        }
        else{
            InsPlan.setPremiumAmt(amt);
        }
        
        String validYear = "^[1-9]?[0-9]{1}$|^30$";
       Pattern pattern4 = Pattern.compile(validYear);
       Matcher matcher4 = pattern4.matcher(Validity);
       if (!matcher4.matches()){
           JOptionPane.showMessageDialog(this,"Enter correct year");
           return;
        }
       else{
           InsPlan.setPlanValidity(Validity);
       }
        system.getInsPlanDirectory().addNewPlan(InsPlan);
        JOptionPane.showMessageDialog(this,"Plan Added");
        CompNameTxtField.setText("");
        PlanNameTxtField.setText("");
        PlanValTxtField.setText("");
        PlanAmtTxtField.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

