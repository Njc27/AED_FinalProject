    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       // TODO add your handling code here:
        String communityName = jCommunityName.getText();
        if(isEdit){
          beforeEdit.setCommunityName(communityName);
            isEdit = false;
            jCommunityName.setText("");
            jButton3.setText("Add Community"); 
          populateTable();  

        }
        else{
         String selectedCity = (String) jCity.getSelectedItem();
        City c = cityLog.searchCity(selectedCity);
        Community newComm = new Community(); 
        newComm.setCommunityName(communityName);
        c.getCommunityCatalog().addCommunity(newComm);
        populateTable();  
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

