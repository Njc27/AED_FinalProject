    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Please Select a Row");
        }
        else{
            String selectedEvent =  (String) model.getValueAt(selectedRow,1);
            selectedeve = system.getEventDirectory().findEventByEventName(selectedEvent);
            jTabbedPane1.setSelectedIndex(0);
            isEdit = true;
            jTextField1.setText(selectedeve.getEventName());
            jTextField2.setText(selectedeve.getAddress());
            jTextField3.setText(selectedeve.getHostName());
            jTextField4.setText(selectedeve.getEmail());
            jTextField5.setText(selectedeve.getPhone());
            jDateChooser1.setDate(selectedeve.getDate());
            jComboBox1.setSelectedItem(selectedeve.getTime());
            jComboBox3.setSelectedItem(selectedeve.getCom());
            jComboBox2.setSelectedItem(selectedeve.getCity());
            
            jButton3.setVisible(false);
            jButton7.setVisible(true);
            jButton4.setVisible(true);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

