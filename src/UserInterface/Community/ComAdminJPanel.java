    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String eventName = jTextField1.getText();
        Date date = jDateChooser1.getCalendar() == null ? null:jDateChooser1.getDate();
        String address = jTextField2.getText();
        String hostName = jTextField3.getText();
        String email = jTextField4.getText();
        String phone = jTextField5.getText();
        String scom = (String) jComboBox3.getSelectedItem();
        String scity = (String)jComboBox2.getSelectedItem();
        City city = system.getCityDirectory().getCityByName(scity);
        Community com = city.getComDir().getCommunityByName(scom);

        if(eventName.trim().length()>0 && hostName.trim().length()>0){
            Event e = new Event();
            e.setAddress(address);
            e.setCity(city);
            e.setCom(com);
            e.setDate(date);
            e.setEmail(email);
            e.setHostName(hostName);
            e.setEventName(eventName);
            e.setPhone(phone);
            e.setTime(scity);

            system.getEventDirectory().addEvent(e);
            JOptionPane.showMessageDialog(this, "New Person Created");
        }
        else{
            JOptionPane.showMessageDialog(this, "Fill all the fields");
        }
        populateTable();

    }//GEN-LAST:event_jButton3ActionPerformed

    
     public void populateTable(){
        for (int i = 0; i< system.getCityDirectory().getCityList().size();i++){
            jComboBox2.addItem(system.getCityDirectory().getCityList().get(i).getCityName());
        }
        
         DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
         model.setRowCount(0);
        
         for (int i =0;i<system.getEventDirectory().getEventList().size();i++){
                        Object rowData[] = new Object[5];
                        rowData[0] = i;
                        rowData[1] = system.getEventDirectory().getEventList().get(i).getEventName();
                        rowData[2] = system.getEventDirectory().getEventList().get(i).getDate();
                        rowData[3] = system.getEventDirectory().getEventList().get(i).getHostName();
                        rowData[4] = system.getEventDirectory().getEventList().get(i).getCom();
                        model.addRow(rowData);
        }
    }
     
