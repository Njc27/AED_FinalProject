    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String city = jCityName.getText();
        if(isEdit){
            beforeEdit.setCityName(city);
            populateTable();
            jCityName.setText("");
            jButton1.setText("Add City");
            isEdit = true;
        }
        else{
        City c = new City();
        c.setCityName(city);
        cityLog.addCity(c);
        populateTable();
        jCityName.setText(""); 
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Please Select a Row");
        }
        else{    
         String city = (String) model.getValueAt(selectedRow,0);
         City selectedCity = cityLog.searchCity(city);
         jCityName.setText(city);
         jButton1.setText("Save");
         beforeEdit = selectedCity;
         isEdit = true;
         
         
//         ManageCommunity manageCommunity = new ManageCommunity(jSplitPane2, cityLog,jPanel2);
//         jSplitPane2.setRightComponent(manageCommunity);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

