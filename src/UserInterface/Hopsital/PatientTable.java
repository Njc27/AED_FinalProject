    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Please Select a Row");
        }
        else{
            String id = (String) model.getValueAt(selectedRow,0);
            People p = hosp.getPatientCatalouge().findPatient(id);
            AddPatient addPatient = new AddPatient(mainPane,patientPane,hosp,comm, true,mainPanel1,patientPanel1,p,new CityCatalouge());
            patientPane.setRightComponent(addPatient);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        System.out.println(hosp);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();
        if(selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Please Select a Row");
        }
        else{
            String id = (String) model.getValueAt(selectedRow,0);
            for(int i = 0 ;i< hosp.getDoctorCataloug().size();i++ ){
                Doctor d = hosp.getDoctorCataloug().get(i);
                for(int j = 0;j < d.getPatientCatalouge().size();j ++){
                   if(d.getPatientCatalouge().get(j).getId().equalsIgnoreCase(id)){
                       d.getPatientCatalouge().removePatient(id);
                       break;
                   }
                }
            }
            hosp.getPatientCatalouge().removePatient(id);
            populateTable();
            System.out.println(hosp);
        }

    }//GEN-LAST:event_jButton7ActionPerformed

    void populateTable(){
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
       model.setRowCount(0);
       for(int i = 0 ;i < hosp.getPatientCatalouge().size();i++){
           System.out.println(hosp.getPatientCatalouge());
           if(hosp.getPatientCatalouge().size() >0){
           Object rowData[] = new Object[2];
           rowData[0] = hosp.getPatientCatalouge().get(i).getId();
           rowData[1] = hosp.getPatientCatalouge().get(i).getName();
           model.addRow(rowData);
           }
       }
    }

