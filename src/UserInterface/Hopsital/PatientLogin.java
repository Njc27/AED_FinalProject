    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean flag = false;
        String userName = jUsername.getText();
        String password = jPassword.getText();
        String selectedCity = (String) jCity.getSelectedItem();
            String selectedCommunity = (String) jCommunity.getSelectedItem();
            City city = cityLog.searchCity(selectedCity);
            Community comm = city.getCommunityCatalog().searchCommunity(selectedCommunity);
            PeopleCatalouge peopLog = comm.getPeopleCatalog();
            for(int i = 0; i < peopLog.size();i++){
                if(peopLog.get(i).getLoginInfo().getUserName().equalsIgnoreCase(userName) && peopLog.get(i).getLoginInfo().getPassword().equals(password)){
//                    CommunityAdmin commAdmin = new CommunityAdmin(jSplitPane2,comm,jPanel2);
//                    jSplitPane2.setRightComponent(commAdmin);
                        ReportPage report = new ReportPage(jSplitPane2,peopLog.get(i),comm,cityLog,city);
                        jSplitPane2.setRightComponent(report);
                        flag = true;
                        break;
            }
                
            }
            if(!flag){
             JOptionPane.showMessageDialog(this, "Invalid UserName or Password");
            }

    }//GEN-LAST:event_jButton1ActionPerformed


