    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        EncounterHistory encounterHistory = patient.getEncounterHistory();
        DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        for(int i = 0; i< encounterHistory.size();i++){
            Object rowData[] = new Object[8];
            rowData[0] = i+1; 
            rowData[1] = df.format(encounterHistory.get(i).getEncounterDate());
            rowData[2] = encounterHistory.get(i).getVitalsigns().getBloodpressure();
            rowData[3] = encounterHistory.get(i).getVitalsigns().getHeartRate();
            rowData[4] = encounterHistory.get(i).getVitalsigns().getTemperature();
            rowData[5] = encounterHistory.get(i).getVitalsigns().getWeight();
            rowData[6] = encounterHistory.get(i).getVitalsigns().getHeight();
            rowData[7] = encounterHistory.get(i).getVitalsigns().getRespiratoryRate();
            model.addRow(rowData);
        }
    }

